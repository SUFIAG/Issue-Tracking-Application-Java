    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            ITS its = ITS.getInstance();
            if (jTextField1.getText().equals("Enter Issue No Here") || !its.validateIssueID(jTextField1.getText())) {
                jLabel3.setText("Kindly enter correct Issue No above");
            } else {
                this.dispose();
                new EditInformation(jTextField1.getText()).setVisible(true);
            }
        }
        catch (Exception e) {
            jLabel3.setText("Kindly enter correct Issue No above");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

