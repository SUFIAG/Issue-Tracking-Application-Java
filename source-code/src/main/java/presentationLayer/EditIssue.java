    // function to display all the issues in the table
    private void displayIssues() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        ITS its = ITS.getInstance();
        Vector<Issue> issues = its.getIssues();
        for (Issue issue : issues) {
            model.addRow(new Object[]{issue.getID(), issue.getDesc(), issue.getAssignee(),
                    issue.getStatus(), issue.getCreationDate()});
        }
    }

